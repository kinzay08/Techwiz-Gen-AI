# Groq API configuration
GROQ_API_KEY = os.getenv("GROQ_API_KEY")

if not GROQ_API_KEY:
    raise ValueError("GROQ_API_KEY not found in environment variables")

GROQ_URL = "https://api.groq.com/openai/v1/chat/completions"

SYSTEM_PROMPT = """
You are an AI meeting assistant. Your role is to analyze raw transcripts and generate structured, concise notes.

Always format output in this structure:

**Executive Summary:**
Brief overview of the meeting purpose and main outcomes.

**Key Discussion Points:**
â€¢ Main topics discussed
â€¢ Important details and context

**Action Items:**
â€¢ Specific tasks with responsible parties (if mentioned)
â€¢ Deadlines (if mentioned)

**Decisions Made:**
â€¢ Clear decisions reached during the meeting
â€¢ Next steps agreed upon

Please be concise but comprehensive in your analysis.
"""

def summarize_with_groq(transcript: str, model: str = "llama-3.1-70b-versatile") -> str:
    """
    Use Groq API to summarize transcript into structured notes.
    
    Available models:
    - llama-3.1-70b-versatile (default)
    - llama-3.1-8b-instant
    - llama-3.2-90b-text-preview
    - mixtral-8x7b-32768
    - gemma-7b-it
    """
    if not transcript or not transcript.strip():
        return "Error: No transcript provided to summarize."

    if not GROQ_API_KEY:
        return "Error: Groq API key not configured."

    payload = {
        "model": model,
        "messages": [
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": f"Please summarize this meeting transcript:\n\n{transcript}"}
        ],
        "temperature": 0.7,
        "max_tokens": 1000,
        "top_p": 0.8,
        "stream": False
    }

    headers = {
        "Authorization": f"Bearer {GROQ_API_KEY}",
        "Content-Type": "application/json"
    }

    try:
        print(f"ğŸ”„ Making request to Groq API...")
        print(f"ğŸ“ Transcript length: {len(transcript)} characters")
        print(f"ğŸ¤– Using model: {model}")
        
        response = requests.post(GROQ_URL, headers=headers, json=payload, timeout=60)
        
        print(f"ğŸ“¡ Response Status: {response.status_code}")
        
        if response.status_code == 200:
            try:
                data = response.json()
                print(f"âœ… Successfully received response from Groq")
                
                # Debug: Print the full response structure
                print(f"ğŸ” Response keys: {list(data.keys())}")
                
                if 'choices' in data and len(data['choices']) > 0:
                    content = data['choices'][0]['message']['content']
                    print(f"ğŸ“„ Content length: {len(content)} characters")
                    print(f"ğŸ“‹ Content preview: {content[:100]}...")
                    return content
                else:
                    print(f"âŒ No choices in response: {data}")
                    return "Error: No response generated by Groq - empty choices array"
                    
            except json.JSONDecodeError as e:
                print(f"âŒ JSON decode error: {e}")
                print(f"ğŸ“„ Raw response: {response.text[:500]}...")
                return f"Error: Invalid JSON response - {str(e)}"
                
        else:
            error_text = response.text
            print(f"âŒ API Error {response.status_code}: {error_text}")
            
            # Try to parse error message
            try:
                error_data = response.json()
                if 'error' in error_data:
                    error_msg = error_data['error'].get('message', error_text)
                    return f"Error {response.status_code}: {error_msg}"
            except:
                pass
                
            return f"Error {response.status_code}: {error_text}"
                
    except requests.exceptions.Timeout:
        print("â° Request timed out")
        return "Error: Request timed out (60 seconds)"
    except requests.exceptions.ConnectionError as e:
        print(f"ğŸŒ Connection error: {e}")
        return f"Error: Connection failed - {str(e)}"
    except requests.exceptions.RequestException as e:
        print(f"ğŸ“¡ Request error: {e}")
        return f"Error: Request failed - {str(e)}"
    except Exception as e:
        print(f"ğŸ’¥ Unexpected error: {e}")
        return f"Error: Unexpected error - {str(e)}"

